#include "3-calc.h"

/**
 * op_add - add two numbers
 * @a: integer
 * @b: integer
 * Return: result
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtract two numbers
 * @a: integer
 * @b: integer
 * Return: result
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiply two numbers
 * @a: integer
 * @b: integer
 * Return: result
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divide two numbers
 * @a: integer
 * @b: integer
 * Return: result
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - get modulus
 * @a: integer
 * @b: integer
 * Return: result
 */
int op_mod(int a, int b)
{
	return (a % b);
}
